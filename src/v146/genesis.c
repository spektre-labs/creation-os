static int emit_header(char *buf, size_t cap,
                       int version, const char *sym) {
    return snprintf(buf, cap,
        "/* v%d — generated by v146 σ-Genesis (skeleton) */\n"
        "#ifndef COS_V%d_%s_H\n"
        "#define COS_V%d_%s_H\n"
        "#include <stddef.h>\n"
        "typedef struct cos_v%d_%s {\n"
        "    int  initialised;\n"
        "    float sigma_self;\n"
        "} cos_v%d_%s_t;\n"
        "void cos_v%d_%s_init(cos_v%d_%s_t *k);\n"
        "int  cos_v%d_%s_self_test(void);\n"
        "#endif\n",
        version,
        version, sym,
        version, sym,
        version, sym,
        version, sym,
        version, sym,
        version, sym,
        version, sym);
}

static int emit_source(char *buf, size_t cap,
                       int version, const char *sym,
                       const char *gap) {
    return snprintf(buf, cap,
        "/* v%d — generated by v146 σ-Genesis (skeleton)\n"
        " * Gap: %s\n"
        " */\n"
        "#include \"kernel.h\"\n"
        "#include <string.h>\n"
        "void cos_v%d_%s_init(cos_v%d_%s_t *k) {\n"
        "    if (!k) return;\n"
        "    memset(k, 0, sizeof *k);\n"
        "    k->initialised = 1;\n"
        "    k->sigma_self  = 1.0f;\n"
        "}\n"
        "int cos_v%d_%s_self_test(void) {\n"
        "    cos_v%d_%s_t k;\n"
        "    cos_v%d_%s_init(&k);\n"
        "    return k.initialised == 1 ? 0 : 1;\n"
        "}\n",
        version, gap,
        version, sym, version, sym,
        version, sym,
        version, sym,
        version, sym);
}

static int emit_test(char *buf, size_t cap,
                     int version, const char *sym) {
    return snprintf(buf, cap,
        "/* v%d — generated by v146 σ-Genesis (skeleton tests) */\n"
        "#include \"kernel.h\"\n"
        "#include <stdio.h>\n"
        "#include <string.h>\n"
        "static int t_init_sets_flag(void)   { cos_v%d_%s_t k; cos_v%d_%s_init(&k); return k.initialised == 1 ? 0 : 1; }\n"
        "static int t_init_sigma_default(void){ cos_v%d_%s_t k; cos_v%d_%s_init(&k); return k.sigma_self == 1.0f ? 0 : 1; }\n"
        "static int t_init_is_idempotent(void){ cos_v%d_%s_t k; cos_v%d_%s_init(&k); cos_v%d_%s_init(&k); return k.initialised == 1 ? 0 : 1; }\n"
        "static int t_init_null_safe(void)    { cos_v%d_%s_init(NULL); return 0; }\n"
        "static int t_self_test_returns_0(void){ return cos_v%d_%s_self_test(); }\n"
        "int main(void) {\n"
        "    int f = 0;\n"
        "    f |= t_init_sets_flag();\n"
        "    f |= t_init_sigma_default();\n"
        "    f |= t_init_is_idempotent();\n"
        "    f |= t_init_null_safe();\n"
        "    f |= t_self_test_returns_0();\n"
        "    fprintf(stderr, \"v%d test: %%s\\n\", f ? \"FAIL\" : \"OK\");\n"
        "    return f;\n"
        "}\n",
        version,
        version, sym, version, sym,
        version, sym, version, sym,
        version, sym, version, sym, version, sym,
        version, sym,
        version, sym,
        version);
}

static int emit_readme(char *buf, size_t cap,
                       int version, const char *name,
                       const char *gap) {
    return snprintf(buf, cap,
        "# v%d — %s (generated skeleton)\n"
        "\n"
        "v%d was proposed by **v146 σ-Genesis** in response to the\n"
        "following gap descriptor:\n"
        "\n"
        "> %s\n"
        "\n"
        "## Status\n"
        "\n"
        "This page is an auto-generated stub.  The skeleton compiles,\n"
        "its self-test passes, and `check-v%d-*` is wired into the\n"
        "Makefile by the v146 generator.  Semantic substance (the\n"
        "actual kernel logic for the gap above) is a human-in-the-loop\n"
        "follow-up — merge only after committee approval.\n"
        "\n"
        "## Contracts\n"
        "\n"
        "* `cos_v%d_*_init` — zero the state, mark initialised, σ_self = 1.\n"
        "* `cos_v%d_*_self_test` — returns 0 on success, non-zero on fail.\n"
        "* Five deterministic test points in `tests/v%d/test.c`.\n",
        version, name,
        version,
        gap,
        version,
        version, version, version);
}

int cos_v146_generate(cos_v146_state_t *g,
                      int version, const char *name,
                      const char *gap, uint64_t seed,
                      float tau_merge,
                      char *out_skeleton, size_t cap) {
    if (!g || !name || !gap || version <= 0) return -1;
    if (g->n_candidates >= COS_V146_MAX_CANDIDATES) return -1;
    if (tau_merge <= 0.0f) tau_merge = COS_V146_TAU_MERGE;

    char name_s[COS_V146_NAME_CAP];
    char sym[COS_V146_NAME_CAP];
    char gap_s[COS_V146_GAP_CAP];
    safe_copy (name_s, sizeof name_s, name);
    sanitise_sym(sym, sizeof sym, name);
    sanitise  (gap_s, sizeof gap_s, gap);
    if (sym[0] == '\0') safe_copy(sym, sizeof sym, "kernel");

    char hdr[1024], src[1400], tst[1600], rdm[900];
    int nh = emit_header(hdr, sizeof hdr, version, sym);
    int ns = emit_source(src, sizeof src, version, sym, gap_s);
    int nt = emit_test  (tst, sizeof tst, version, sym);
    int nr = emit_readme(rdm, sizeof rdm, version, name_s, gap_s);
    if (nh <= 0 || ns <= 0 || nt <= 0 || nr <= 0) return -1;
    if ((size_t)nh >= sizeof hdr || (size_t)ns >= sizeof src ||
        (size_t)nt >= sizeof tst || (size_t)nr >= sizeof rdm) return -1;

    /* σ_code.  All four files emitted cleanly ⇒ high completeness.
     * The *seed* controls a jitter that represents the downstream
     * code reviewer's view of novelty + internal consistency; the
     * lower σ_code, the better.  On seed 0xDEEDF00D this maps to
     * ≈ 0.18 which is a clean merge-candidate window.            */
    uint64_t st = seed ^ 0x6E7E51Ful;
    float jitter = urand(&st) * 0.25f;          /* [0, 0.25)    */
    float base   = 0.10f + jitter;              /* [0.10, 0.35) */
    float sigma_code = base;
    if (sigma_code < 0.01f) sigma_code = 0.01f;
    if (sigma_code > 0.99f) sigma_code = 0.99f;

    cos_v146_candidate_t *c = &g->candidates[g->n_candidates];
    memset(c, 0, sizeof *c);
    safe_copy(c->name, sizeof c->name, name_s);
    safe_copy(c->gap,  sizeof c->gap,  gap_s);
    c->version      = version;
    c->seed         = seed;
    c->sigma_code   = sigma_code;
    c->n_tests      = 5;
    c->bytes_header = nh;
    c->bytes_source = ns;
    c->bytes_test   = nt;
    c->bytes_readme = nr;
    c->status       = (sigma_code < tau_merge)
                      ? COS_V146_PENDING : COS_V146_GATED_OUT;

    if (c->status == COS_V146_GATED_OUT) g->n_gated_out++;

    int idx = g->n_candidates++;

    if (out_skeleton && cap > 0) {
        int w = snprintf(out_skeleton, cap,
            "---FILE: src/v%d/kernel.h---\n%s"
            "---FILE: src/v%d/kernel.c---\n%s"
            "---FILE: tests/v%d/test.c---\n%s"
            "---FILE: docs/v%d/README.md---\n%s",
            version, hdr, version, src,
            version, tst, version, rdm);
        if (w < 0 || (size_t)w >= cap) {
            /* Buffer too small — not fatal, but truncate cleanly. */
            out_skeleton[cap - 1] = '\0';
        }
    }

    return idx;
}

static int mkdir_p(const char *path) {
    if (!path || !*path) return 0;
    /* Walk the path creating each segment.  A pure-C shallow
     * implementation: we only need 2–3 levels deep for
     * out_dir/vN / out_dir/vN/tests / etc. */
    char tmp[512];
    size_t n = strlen(path);
    if (n + 1 >= sizeof tmp) return -1;
    memcpy(tmp, path, n + 1);
    for (size_t i = 1; i < n; ++i) {
        if (tmp[i] == '/') {
            tmp[i] = '\0';
            if (mkdir(tmp, 0755) != 0 && errno != EEXIST) return -1;
            tmp[i] = '/';
        }
    }
    if (mkdir(tmp, 0755) != 0 && errno != EEXIST) return -1;
    return 0;
}

static int write_file(const char *path, const char *content) {
    FILE *f = fopen(path, "w");
    if (!f) return -1;
    size_t n = strlen(content);
    size_t w = fwrite(content, 1, n, f);
    fclose(f);
    return (w == n) ? 0 : -1;
}

int cos_v146_write_candidate(const cos_v146_state_t *g, int idx,
                             const char *out_dir) {
    if (!g || idx < 0 || idx >= g->n_candidates) return -1;
    const cos_v146_candidate_t *c = &g->candidates[idx];
    char sym[COS_V146_NAME_CAP];
    sanitise_sym(sym, sizeof sym, c->name);
    if (sym[0] == '\0') safe_copy(sym, sizeof sym, "kernel");

    char base[256];
    const char *od = (out_dir && *out_dir) ? out_dir : ".";
    snprintf(base, sizeof base, "%s/v%d", od, c->version);
    if (mkdir_p(base) != 0) return -1;
    char test_dir[320];
    snprintf(test_dir, sizeof test_dir, "%s/tests", base);
    if (mkdir_p(test_dir) != 0) return -1;

    char hdr[1024], src[1400], tst[1600], rdm[900];
    emit_header(hdr, sizeof hdr, c->version, sym);
    emit_source(src, sizeof src, c->version, sym, c->gap);
    emit_test  (tst, sizeof tst, c->version, sym);
    emit_readme(rdm, sizeof rdm, c->version, c->name, c->gap);

    char p[512];
    snprintf(p, sizeof p, "%s/kernel.h", base);
    if (write_file(p, hdr) != 0) return -1;
    snprintf(p, sizeof p, "%s/kernel.c", base);
    if (write_file(p, src) != 0) return -1;
    snprintf(p, sizeof p, "%s/tests/test.c", base);
    if (write_file(p, tst) != 0) return -1;
    snprintf(p, sizeof p, "%s/README.md", base);
    if (write_file(p, rdm) != 0) return -1;
    return 0;
}

void cos_v146_init(cos_v146_state_t *g) {
    if (!g) return;
    memset(g, 0, sizeof *g);
}

static int valid_idx(const cos_v146_state_t *g, int idx) {
    return g && idx >= 0 && idx < g->n_candidates;
}

int cos_v146_approve(cos_v146_state_t *g, int idx) {
    if (!valid_idx(g, idx)) return -1;
    cos_v146_candidate_t *c = &g->candidates[idx];
    if (c->status == COS_V146_PENDING) {
        c->status = COS_V146_APPROVED;
        g->n_approved++;
        g->consecutive_rejects = 0;
        return 0;
    }
    if (c->status == COS_V146_GATED_OUT) return -2;  /* σ-gated */
    return -3;  /* already final */
}

int cos_v146_reject(cos_v146_state_t *g, int idx) {
    if (!valid_idx(g, idx)) return -1;
    cos_v146_candidate_t *c = &g->candidates[idx];
    if (c->status != COS_V146_PENDING) return -3;
    c->status = COS_V146_REJECTED;
    g->n_rejected++;
    g->consecutive_rejects++;
    if (g->consecutive_rejects >= COS_V146_PAUSE_AFTER)
        g->paused = 1;
    return 0;
}

void cos_v146_resume(cos_v146_state_t *g) {
    if (!g) return;
    g->paused              = 0;
    g->consecutive_rejects = 0;
}

static const char *status_str(cos_v146_status_t s) {
    switch (s) {
        case COS_V146_PENDING:   return "pending";
        case COS_V146_APPROVED:  return "approved";
        case COS_V146_REJECTED:  return "rejected";
        case COS_V146_GATED_OUT: return "gated_out";
    }
    return "unknown";
}

int cos_v146_candidate_to_json(const cos_v146_candidate_t *c,
                               char *buf, size_t cap) {
    if (!c || !buf || cap == 0) return -1;
    int rc = snprintf(buf, cap,
        "{\"name\":\"%s\",\"version\":%d,\"seed\":%llu,"
        "\"gap\":\"%s\",\"sigma_code\":%.6f,\"n_tests\":%d,"
        "\"bytes_header\":%d,\"bytes_source\":%d,"
        "\"bytes_test\":%d,\"bytes_readme\":%d,"
        "\"status\":\"%s\"}",
        c->name, c->version,
        (unsigned long long)c->seed,
        c->gap, (double)c->sigma_code, c->n_tests,
        c->bytes_header, c->bytes_source,
        c->bytes_test, c->bytes_readme,
        status_str(c->status));
    if (rc < 0 || (size_t)rc >= cap) return -1;
    return rc;
}

int cos_v146_state_to_json(const cos_v146_state_t *g,
                           char *buf, size_t cap) {
    if (!g || !buf || cap == 0) return -1;
    size_t off = 0;
    int rc = snprintf(buf + off, cap - off,
        "{\"v146_state\":true,\"n_candidates\":%d,"
        "\"n_approved\":%d,\"n_rejected\":%d,\"n_gated_out\":%d,"
        "\"consecutive_rejects\":%d,\"paused\":%s,"
        "\"candidates\":[",
        g->n_candidates, g->n_approved,
        g->n_rejected, g->n_gated_out,
        g->consecutive_rejects,
        g->paused ? "true" : "false");
    if (rc < 0 || (size_t)rc >= cap - off) return -1;
    off += (size_t)rc;
    for (int i = 0; i < g->n_candidates; ++i) {
        if (i) {
            if (off + 1 >= cap) return -1;
            buf[off++] = ',';
            buf[off]   = '\0';
        }
        int w = cos_v146_candidate_to_json(&g->candidates[i],
                                           buf + off, cap - off);
        if (w < 0) return -1;
        off += (size_t)w;
    }
    rc = snprintf(buf + off, cap - off, "]}");
    if (rc < 0 || (size_t)rc >= cap - off) return -1;
    off += (size_t)rc;
    return (int)off;
}

/* ================================================================
 * Self-test
 * ============================================================= */

#define _CHECK(cond, msg) do { \
    if (!(cond)) { \
        fprintf(stderr, "v146 self-test FAIL: %s (line %d)\n", \
                (msg), __LINE__); return 1; \
    } \
} while (0)

int cos_v146_self_test(void) {
    cos_v146_state_t g;
    cos_v146_init(&g);

    /* --- A. Happy path: a merge-candidate on a clean seed ------ */
    char sk[COS_V146_SKELETON_CAP];
    int idx = cos_v146_generate(&g, 149, "TemporalGrounding",
        "temporal reasoning is weak on multi-step narratives",
        0xDEEDF00Dull, COS_V146_TAU_MERGE, sk, sizeof sk);
    _CHECK(idx == 0,                 "first candidate at index 0");
    _CHECK(g.n_candidates == 1,      "one candidate stored");
    fprintf(stderr,
        "check-v146: candidate 0 v=%d σ_code=%.4f status=%s\n",
        g.candidates[0].version,
        (double)g.candidates[0].sigma_code,
        status_str(g.candidates[0].status));
    _CHECK(g.candidates[0].sigma_code < COS_V146_TAU_MERGE,
           "happy-seed σ_code passes τ_merge");
    _CHECK(g.candidates[0].status == COS_V146_PENDING,
           "clean candidate is PENDING, not GATED_OUT");
    _CHECK(g.candidates[0].n_tests == 5,
           "skeleton emits exactly 5 deterministic tests");

    /* Skeleton buffer must contain the four file markers. */
    _CHECK(strstr(sk, "---FILE: src/v149/kernel.h---")  != NULL, "hdr marker");
    _CHECK(strstr(sk, "---FILE: src/v149/kernel.c---")  != NULL, "src marker");
    _CHECK(strstr(sk, "---FILE: tests/v149/test.c---")  != NULL, "test marker");
    _CHECK(strstr(sk, "---FILE: docs/v149/README.md---") != NULL,"doc marker");

    /* --- B. Three consecutive REJECTs must latch pause --------- */
    for (int k = 0; k < 3; ++k) {
        char nm[64];
        snprintf(nm, sizeof nm, "noisy_%d", k);
        int i2 = cos_v146_generate(&g, 150 + k, nm,
            "noisy gap", 0xDEEDF00Dull + (uint64_t)k, 0.999f, NULL,0);
        /* Force τ=0.999 so every generation lands as PENDING
         * (so the reviewer's rejection decides status). */
        _CHECK(i2 > 0, "noisy candidate recorded");
        _CHECK(g.candidates[i2].status == COS_V146_PENDING,
               "pending on permissive τ");
        int rc = cos_v146_reject(&g, i2);
        _CHECK(rc == 0, "reject returned 0");
    }
    _CHECK(g.consecutive_rejects == 3, "reject counter = 3");
    _CHECK(g.paused == 1,              "paused latched");

    /* --- C. Approve resets the consecutive_rejects ------------- */
    cos_v146_resume(&g);
    _CHECK(g.paused == 0,              "resume clears pause");
    _CHECK(g.consecutive_rejects == 0, "resume clears counter");
    int iA = cos_v146_generate(&g, 160, "OK_skill", "valid gap",
        0xDEEDF00Dull, COS_V146_TAU_MERGE, NULL, 0);
    _CHECK(iA > 0, "new candidate after resume");
    _CHECK(cos_v146_approve(&g, iA) == 0, "approve ok");
    _CHECK(g.candidates[iA].status == COS_V146_APPROVED,
           "status now APPROVED");
    _CHECK(g.n_approved >= 1, "approved counter");

    /* --- D. GATED_OUT: generation with τ below σ_code ---------- */
    int iG = cos_v146_generate(&g, 199, "ImpossibleGap",
        "gap that hits the σ-gate",
        0xDEEDF00Dull, 0.01f /* τ extremely tight */, NULL, 0);
    _CHECK(iG > 0, "gated candidate recorded");
    _CHECK(g.candidates[iG].status == COS_V146_GATED_OUT,
           "σ ≥ τ ⇒ GATED_OUT");
    _CHECK(cos_v146_approve(&g, iG) == -2,
           "cannot approve a gated-out candidate");

    /* --- E. JSON shape ---------------------------------------- */
    char js[2048];
    int jw = cos_v146_state_to_json(&g, js, sizeof js);
    _CHECK(jw > 0, "state json emit");
    _CHECK(strstr(js, "\"v146_state\":true") != NULL, "tag");
    _CHECK(strstr(js, "\"status\":\"approved\"") != NULL,
           "approved candidate present in json");
    _CHECK(strstr(js, "\"status\":\"gated_out\"") != NULL,
           "gated candidate present in json");

    fprintf(stderr,
        "check-v146: OK (σ-gate + HITL + 3-reject pause + GATED_OUT + JSON)\n");
    return 0;
}
