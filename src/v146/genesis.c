/* SplitMix64 — shared PRNG spec with v139/v143/v145. */
static uint64_t splitmix(uint64_t *s) {
    uint64_t z = (*s += 0x9E3779B97F4A7C15ULL);
    z = (z ^ (z >> 30)) * 0xBF58476D1CE4E5B9ULL;
    z = (z ^ (z >> 27)) * 0x94D049BB133111EBULL;
    return z ^ (z >> 31);
}
static float urand(uint64_t *s) {
    uint64_t x = splitmix(s);
    return (float)((x >> 40) & 0xFFFFFFu) / (float)0xFFFFFFu;
}

static void safe_copy(char *dst, size_t cap, const char *src) {
    if (!dst || cap == 0) return;
    if (!src) { dst[0] = '\0'; return; }
    size_t i = 0;
    for (; i + 1 < cap && src[i]; ++i) dst[i] = src[i];
    dst[i] = '\0';
}

/* Sanitise a user-supplied gap descriptor / name for safe
 * embedding into generated C source: keep [A-Za-z0-9_ .,-] and
 * replace everything else with '_'. */
static void sanitise(char *dst, size_t cap, const char *src) {
    if (!dst || cap == 0) return;
    if (!src) { dst[0] = '\0'; return; }
    size_t j = 0;
    for (size_t i = 0; src[i] && j + 1 < cap; ++i) {
        char c = src[i];
        int ok = (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') ||
                 (c >= '0' && c <= '9') || c == '_' || c == ' ' ||
                 c == '.' || c == ',' || c == '-';
        dst[j++] = ok ? c : '_';
    }
    dst[j] = '\0';
}

/* Version-suffix allowed in C symbol names — strip everything
 * non-[A-Za-z0-9_]. */
static void sanitise_sym(char *dst, size_t cap, const char *src) {
    if (!dst || cap == 0) return;
    if (!src) { dst[0] = '\0'; return; }
    size_t j = 0;
    for (size_t i = 0; src[i] && j + 1 < cap; ++i) {
        char c = src[i];
        int ok = (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') ||
                 (c >= '0' && c <= '9') || c == '_';
        if (ok) dst[j++] = c;
    }
    dst[j] = '\0';
}

